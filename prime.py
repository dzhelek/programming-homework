# напишете програма с фунцкия, проверяваща дали едно цяло число е просто
# функцията върща резултат 1, ако числото е просто, и 0, ако не е
# да се принтират всички прости числа от интервала [2; цялото число]
def is_prime(n):
    if n < 2:
        return 0
    for i in range(2, n):
        if n % i == 0:
            return 0
    return 1

n = 133
print([x for x in range(n+1) if is_prime(x)])